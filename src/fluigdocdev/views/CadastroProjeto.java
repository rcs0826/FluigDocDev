    private void tbArqMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbArqMouseClicked
        // TODO add your handling code here:
        if (!tbArq.getValueAt(tbArq.getSelectedRow(), 0).toString().equals("")) {
            int isDel = JOptionPane.showConfirmDialog(null,
                    "Deseja Deletar o Arquivo: "
                    + tbArq.getValueAt(tbArq.getSelectedRow(), 1).toString()
                    + " - Tipo: "
                    + tbArq.getValueAt(tbArq.getSelectedRow(), 5).toString()
            );
            System.out.println("isDel: " + isDel);
            if (isDel == 0) {
                this.arq.setId(tbArq.getValueAt(tbArq.getSelectedRow(), 0).toString());
                try {
                    this.arq.setDelArq();
                    this.tbArq.setModel(this.arq.getArqModel((String) this.cbProjeto.getSelectedItem()));
                } catch (SQLException ex) {
                    Logger.getLogger(CadastroProjeto.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_tbArqMouseClicked

    private void tbProjetosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProjetosMouseClicked
        // TODO add your handling code here:
        if (!tbProjetos.getValueAt(tbProjetos.getSelectedRow(), 0).toString().equals("")) {
            int isDel = JOptionPane.showConfirmDialog(null,
                    "Deseja Deletar o Projeto: "
                    + tbProjetos.getValueAt(tbProjetos.getSelectedRow(), 1).toString()
            );
            System.out.println("isDel: " + isDel);
            if (isDel == 0) {
                String descCli = (String) this.cbCliente.getSelectedItem();
                this.proj.setId(tbProjetos.getValueAt(tbProjetos.getSelectedRow(), 0).toString());
                try {                   
                   this.proj.setDelArq();
                   //this.cbProjeto.setModel(this.sql.getResultComboBoxModel(this.proj.getSelProjCB()));
                   this.cbProjeto.setModel(this.proj.getArqModelCB(descCli));
                   this.tbProjetos.setModel(this.proj.getArqModel(descCli));
                } catch (SQLException ex) {
                    Logger.getLogger(CadastroProjeto.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_tbProjetosMouseClicked


