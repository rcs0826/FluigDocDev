    private void cbClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbClienteActionPerformed
        // TODO add your handling code here:
        String descCli = (String) this.cbCliente.getSelectedItem();  
        cli.setNome(descCli);
        try {
            String url = sql.getUnicValue(cli.getSelUrlCli());          
            File file = new File(url);
            FileSystemModel model = new FileSystemModel(file);
            this.jTree1.setModel(model);            
            this.cbProjeto.setModel(this.proj.getArqModelCB(descCli));
            this.tbProjetos.setModel(this.proj.getArqModel(descCli));
        } catch (Exception e) {
            System.err.println("Erro: " + e.getMessage());
        }

    }//GEN-LAST:event_cbClienteActionPerformed

    private void btNovoProjetoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNovoProjetoActionPerformed
        // TODO add your handling code here:
        if (this.cbCliente.getSelectedItem().equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um Cliente!");
        } else {
            String projeto = JOptionPane.showInputDialog("Digite o Nome do Novo Projeto");
            if (projeto.trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Nome de Projeto est√° em branco");
            } else {
                try {
                    String descCli = (String) this.cbCliente.getSelectedItem();
                    this.proj.setProject(descCli, projeto);
                    this.cbProjeto.setModel(this.proj.getArqModelCB(descCli));
                    this.tbProjetos.setModel(this.proj.getArqModel(descCli));
                } catch (SQLException ex) {
                    Logger.getLogger(CadastroProjeto.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }
    }//GEN-LAST:event_btNovoProjetoActionPerformed

    private void jTree1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTree1MouseClicked
        if (evt.getClickCount() == 2) {
            if (!this.cbProjeto.getSelectedItem().equals("Selecione")) {
                try {
                    this.arq.setArq(this.jTree1.getSelectionPath(), (String) this.cbProjeto.getSelectedItem());
                    this.tbArq.setModel(this.arq.getArqModel((String) this.cbProjeto.getSelectedItem()));
                } catch (SQLException ex) {
                    Logger.getLogger(CadastroProjeto.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jTree1MouseClicked

